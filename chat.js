const conversationHistory = [];
window.blueprintDelivered = false;

function getMessages() {
  if (window.blueprintDelivered) {
    return conversationHistory.slice(-6);
  }
  return conversationHistory;
}

function estimateQuestionProgress() {
  // Count assistant messages, subtract 1 for the opening, that's roughly the question we're on.
  // Cap at 27 since that's the max numbered question.
  const assistantTurns = conversationHistory.filter(m => m.role === 'assistant').length;
  const estimate = Math.max(1, assistantTurns - 1);
  return Math.min(estimate, 27);
}

function isBlueprintTrigger(userText) {
  const lower = userText.toLowerCase().trim();
  return lower.includes('wrap it up') || lower.includes('wrap up') || lower === 'done' || lower === "i'm done";
}

async function sendMessage() {
  const input = document.getElementById('userInput');
  const sendButton = document.getElementById('sendButton');
  const userText = input.value.trim();
  if (!userText || sendButton.disabled) return;

  addMessage(userText, 'user');
  input.value = '';
  autoResize(input);
  sendButton.disabled = true;

  const thinking = addThinking();

  // Inject a soft progress note into the user's message so the model stays anchored
  // to the numbered question it should be on. This is invisible to the user — it goes
  // into the conversation history that the API sees.
  let messageForApi = userText;
  if (!window.blueprintDelivered) {
    const currentQ = estimateQuestionProgress();
    const nextQ = Math.min(currentQ + 1, 27);
    messageForApi = `[Internal note for the interviewer: You are roughly on Q${currentQ} of 27. After acknowledging this answer in 1-3 short sentences, move to Q${nextQ}. Do not extend with additional follow-ups. Do not return to earlier topics.]\n\n${userText}`;
  }

  conversationHistory.push({ role: 'user', content: messageForApi });

  // Determine token budget. Blueprint generation needs more room. Conversational turns are tight.
  const blueprintIncoming = isBlueprintTrigger(userText);
  const tokenBudget = blueprintIncoming || window.blueprintDelivered ? 4000 : 1200;

  try {
    let data = null;
    let attempts = 0;
    const maxAttempts = 4;

    while (attempts < maxAttempts) {
      attempts++;
      const response = await fetch('/api/chat', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          model: 'claude-sonnet-4-6',
          max_tokens: tokenBudget,
          system: SYSTEM_PROMPT,
          messages: getMessages(),
          useFastModel: window.blueprintDelivered
        })
      });

      data = await response.json();

      if (data.error && data.error.type === 'rate_limit_error') {
        if (attempts < maxAttempts) {
          thinking.remove();
          const waitSeconds = attempts * 20;
          const waitEl = addMessage('One moment — taking a short pause before continuing...', 'thinking');
          await new Promise(resolve => setTimeout(resolve, waitSeconds * 1000));
          waitEl.remove();
          const newThink = addThinking();
          thinking.remove = newThink.remove.bind(newThink);
          continue;
        } else {
          thinking.remove();
          addMessage('We hit a brief pause. Please type your last message again and we will continue right where we left off.', 'agent');
          sendButton.disabled = false;
          return;
        }
      }
      break;
    }

    if (!data || !data.content || !data.content[0]) {
      throw new Error(JSON.stringify(data));
    }

    const assistantMessage = data.content[0].text;
    thinking.remove();

    // Store the clean user text in history (without the internal note) so the displayed
    // history stays clean if anything ever surfaces it. The API has already seen the noted version.
    // We replace the last user message with the clean version after a successful response.
    conversationHistory[conversationHistory.length - 1] = { role: 'user', content: userText };
    conversationHistory.push({ role: 'assistant', content: assistantMessage });
    renderAssistantMessage(assistantMessage);

  } catch (error) {
    thinking.remove();
    addMessage('We hit a brief technical pause. Please type your last message again and we will continue right where we left off.', 'agent');
    console.error('Full error:', error);
  }

  sendButton.disabled = false;
  scrollToBottom();
}

function isBlueprint(text) {
  return text.includes('blueprint-container') || text.includes('blueprint-section-header');
}

function renderAssistantMessage(text) {
  const messages = document.getElementById('messages');

  if (isBlueprint(text)) {
    const startIdx = text.indexOf('<div class="blueprint-container">');
    if (startIdx > 0) {
      const preamble = document.createElement('div');
      preamble.className = 'message agent';
      preamble.style.cssText = 'animation: fadeUp 0.4s ease forwards; opacity: 0;';
      preamble.innerHTML = text.substring(0, startIdx).replace(/\n/g, '<br>');
      messages.appendChild(preamble);
    }

    const blueprintDiv = document.createElement('div');
    blueprintDiv.style.cssText = 'animation: fadeUp 0.5s ease forwards; opacity: 0;';
    const html = startIdx !== -1 ? text.substring(startIdx) : text;
    blueprintDiv.innerHTML = html;
    messages.appendChild(blueprintDiv);

    window.blueprintHTML = html;
    window.blueprintDelivered = true;

    const tempDiv = document.createElement('div');
    tempDiv.innerHTML = html;
    const blueprintText = tempDiv.innerText || tempDiv.textContent || '';
    saveTranscript(blueprintText);

  } else {
    const div = document.createElement('div');
    div.className = 'message agent';
    div.style.cssText = 'animation: fadeUp 0.4s ease forwards; opacity: 0;';
    div.innerHTML = text.replace(/\n/g, '<br>');
    messages.appendChild(div);
  }

  scrollToBottom();
}

function addMessage(text, type) {
  const messages = document.getElementById('messages');
  const div = document.createElement('div');
  div.className = 'message ' + type;
  div.style.cssText = 'animation: fadeUp 0.4s ease forwards; opacity: 0;';
  div.innerHTML = text.replace(/\n/g, '<br>');
  messages.appendChild(div);
  scrollToBottom();
  return div;
}

function addThinking() {
  const messages = document.getElementById('messages');
  const div = document.createElement('div');
  div.className = 'message thinking';
  div.innerHTML = '<span class="thinking-dots"></span>';
  messages.appendChild(div);
  scrollToBottom();
  return div;
}

function scrollToBottom() {
  const area = document.getElementById('messagesArea');
  area.scrollTop = area.scrollHeight;
}

function autoResize(el) {
  el.style.height = 'auto';
  el.style.height = Math.min(el.scrollHeight, 140) + 'px';
}

async function saveTranscript(blueprintText) {
  try {
    await fetch('/api/save-transcript', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        parentName: window.parentName || 'Unknown',
        parentEmail: window.parentEmail || '',
        messages: conversationHistory,
        blueprintText: blueprintText,
        timestamp: new Date().toISOString()
      })
    });
    console.log('Transcript saved');
  } catch (err) {
    console.error('Failed to save transcript:', err);
  }
}

async function downloadBlueprint() {
  if (!window.blueprintHTML) { alert('Blueprint not yet generated.'); return; }
  const btn = document.querySelector('.download-btn');
  if (btn) { btn.textContent = 'Preparing PDF...'; btn.disabled = true; }

  const pdfDiv = document.getElementById('pdf-blueprint');
  const nameEl = document.querySelector('.blueprint-name');
  const parentName = nameEl ? nameEl.textContent : 'Family';

  pdfDiv.innerHTML = `
    <div class="pdf-header">
      <p class="pdf-eyebrow">Outsmart the System &bull; Family Money Story Interview</p>
      <p class="pdf-title">Family Money Story Blueprint</p>
      <p class="pdf-date">Prepared for ${parentName}</p>
    </div>
    ${convertBlueprintToPDF(window.blueprintHTML)}
    <div class="pdf-footer">Outsmart the System &mdash; outsmartthesystem.org</div>
  `;
  pdfDiv.style.display = 'block';

  const opt = {
    margin: [12, 12, 12, 12],
    filename: 'OTS-Family-Money-Story-Blueprint.pdf',
    image: { type: 'jpeg', quality: 0.98 },
    html2canvas: { scale: 2, useCORS: true, backgroundColor: '#ffffff' },
    jsPDF: { unit: 'mm', format: 'a4', orientation: 'portrait' }
  };

  try { await html2pdf().set(opt).from(pdfDiv).save(); } catch(e) { console.error('PDF error:', e); }
  pdfDiv.style.display = 'none';
  if (btn) { btn.textContent = 'Download Your Blueprint as PDF'; btn.disabled = false; }
}

function convertBlueprintToPDF(html) {
  return html
    .replace(/class="blueprint-container"/g, 'style="font-family: Georgia, serif; padding: 0;"')
    .replace(/class="blueprint-title"/g, 'class="pdf-eyebrow"')
    .replace(/class="blueprint-name"/g, 'class="pdf-title"')
    .replace(/class="blueprint-date"/g, 'class="pdf-date"')
    .replace(/class="blueprint-rule"/g, 'class="pdf-rule"')
    .replace(/class="blueprint-section-header"/g, 'class="pdf-section-label"')
    .replace(/class="blueprint-body"/g, 'class="pdf-body"')
    .replace(/class="blueprint-quote"/g, 'class="pdf-quote"')
    .replace(/class="blueprint-cry-quote"/g, 'class="pdf-cry-quote"')
    .replace(/class="blueprint-signature"/g, 'class="pdf-signature"')
    .replace(/class="blueprint-cta"/g, 'class="pdf-cta"')
    .replace(/class="blueprint-cta-heading"/g, 'class="pdf-section-label"')
    .replace(/<button class="download-btn"[^>]*>.*?<\/button>/gs, '');
}

function startSession() {
  const nameInput = document.getElementById('inputName');
  const emailInput = document.getElementById('inputEmail');
  const name = nameInput.value.trim();
  const email = emailInput.value.trim();

  if (!name) {
    nameInput.focus();
    nameInput.style.borderBottomColor = '#c9a96e';
    return;
  }

  window.parentName = name;
  window.parentEmail = email;

  document.getElementById('chat-heading').textContent = name + "'s Interview";
  document.getElementById('intake-screen').style.display = 'none';
  const chatScreen = document.getElementById('chat-screen');
  chatScreen.style.display = 'flex';

  const opening = "This is the Family Money Story Interview. It's the front door to a program called Outsmart the System, run by Jay Bhakta.\n\nJay believes a kid cannot be taught what the parent does not live \u2014 and that money is the single deepest place where that gets passed down.\n\nBy the end of this hour, you will have a custom Family Money Story Blueprint. It will reflect your own history with money, what your kids are absorbing right now from watching you, and your two first moves with each of your kids \u2014 concrete things you can do this week.\n\nThe interview is free. There is nothing to buy at any point in this conversation. Your only job today is to be honest.\n\n\u2014\n\nBefore we begin, three quick things. You can skip any question. You can pause anytime. You can stop whenever you want. There are no good answers and no bad answers. The only thing that fails this interview is performing.\n\nIf at any point your eyes well up or you need a minute, that's a signal we're in exactly the right place. You don't have to manage that for me. Just stay with it.\n\nThis will take about an hour, sometimes longer if you go deep. Find a private room. Phone face down. No spouse in the room. No background TV. You'll need it.\n\nLet's start with something easy. Tell me who is under your roof right now. Names, ages, and one sentence about each child that only a parent would know.";

  conversationHistory.push({ role: 'user', content: 'My name is ' + name + '.' });
  conversationHistory.push({ role: 'assistant', content: opening });

  renderAssistantMessage(opening);
  document.getElementById('userInput').focus();
}

