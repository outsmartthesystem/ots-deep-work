// The sentinel marker the model outputs when the interview is complete.
// The wrapper detects this marker, strips it from the displayed message, and
// fires the webhook to send the transcript to Make.
const COMPLETION_SENTINEL = '[INTERVIEW_COMPLETE]';

function isInterviewComplete(assistantMessage) {
  // Detection is sentinel-only as of v12.12. The previous fallback that matched
  // the closing-sentence string proved unreliable — the model sometimes echoed
  // the closing sentence prematurely (in drafts, in mid-interview wrap-up
  // attempts, or when it confused itself), which fired the webhook with empty
  // state and sent Jay blank emails.
  //
  // Sentinel-only means: if the model fails to emit [INTERVIEW_COMPLETE] at
  // the very end, no email fires. That's a visible failure (Jay notices the
  // missing email and we debug) rather than a silent failure (Jay gets blank
  // data and assumes something else broke). Visible failures are recoverable;
  // silent corruption is not.
  return assistantMessage.includes(COMPLETION_SENTINEL);
}

function stripSentinel(message) {
  // Remove the sentinel marker so the parent doesn't see it in the chat.
  return message.replace(COMPLETION_SENTINEL, '').trim();
}

function stripFeedbackBlockForDisplay(message) {
  // The model emits the FEEDBACK FOR JAY block as part of the final response,
  // because the block contains structured data Jay needs to see in the email.
  // But the parent should NOT see this block in their chat — it's internal
  // tooling, not parent-facing content.
  //
  // This function strips the FEEDBACK FOR JAY block from the parent's visible
  // chat while leaving the closing message intact. The stripped block is still
  // in the original message string that gets stored in conversationHistory and
  // sent in the webhook payload — the wrapper just doesn't render it.
  //
  // Detection: find the start of the FEEDBACK FOR JAY block (matching either
  // bold-wrapped or plain markdown title) and the start of the closing message
  // ("That's everything"). Remove everything between them.

  // Find the start of the FEEDBACK FOR JAY block. Match with or without
  // bold markdown asterisks, with the dash literal or em-dash variant.
  const feedbackStart = message.search(/\*?\*?FEEDBACK FOR JAY\b/i);
  if (feedbackStart === -1) {
    return message; // no block present, nothing to strip
  }

  // Find the start of the closing message.
  const closingStart = message.indexOf("That's everything");
  if (closingStart === -1 || closingStart <= feedbackStart) {
    // No closing message found after the block, or the block appeared after
    // closing (impossible in valid output but defensive). Strip everything
    // from the block start onward to be safe.
    return message.substring(0, feedbackStart).trim();
  }

  // Keep everything before the block plus everything from the closing onward.
  // Trim the optional "---" separator that the prompt template includes.
  const before = message.substring(0, feedbackStart).trim();
  const after = message.substring(closingStart).trim();
  return (before ? before + '\n\n' : '') + after;
}

function buildTranscriptPayload() {
  // Assemble a clean, structured payload of everything Jay needs to see.
  // Field names match the existing Make Google Sheet column structure (camelCase).
  // Make receives this as JSON and writes a row to the Sheet plus sends the email.

  // Build a human-readable transcript by walking conversationHistory.
  const readableTranscript = conversationHistory.map(turn => {
    const speaker = turn.role === 'user' ? 'PARENT' : 'INTERVIEWER';
    // Strip any internal notes that may have been left in the user content.
    let content = turn.content;
    if (content.includes('[Internal note for the interviewer:')) {
      // The internal-note injection format is: [Internal note...]\n\n[actual user message]
      const parts = content.split('\n\n');
      content = parts[parts.length - 1];
    }
    return `${speaker}:\n${content}`;
  }).join('\n\n─────────────────────────────────────\n\n');

  // The final assistant message contains the Blueprint and FEEDBACK FOR JAY block.
  // It's the most important single piece for Jay's review.
  const lastAssistantMessage = [...conversationHistory]
    .reverse()
    .find(turn => turn.role === 'assistant');
  const finalMessage = lastAssistantMessage ? lastAssistantMessage.content : '';

  // Build a short snippet from the Blueprint for at-a-glance scanning in the
  // Google Sheet. Pull the first ~400 characters of the Blueprint section,
  // which usually captures the WHO YOU ARE paragraph. The full content is
  // available in the fullTranscript and final_message fields.
  let blueprintSnippet = '';
  const blueprintStart = finalMessage.indexOf('blueprint-name');
  if (blueprintStart !== -1) {
    // Pull a chunk after the title block.
    const afterTitle = finalMessage.substring(blueprintStart);
    // Strip HTML tags for a plain-text snippet readable in a spreadsheet cell.
    const plainText = afterTitle.replace(/<[^>]+>/g, ' ').replace(/\s+/g, ' ').trim();
    blueprintSnippet = plainText.substring(0, 400) + (plainText.length > 400 ? '...' : '');
  } else {
    // Fallback: just take the first 400 chars of the final message.
    const plainText = finalMessage.replace(/<[^>]+>/g, ' ').replace(/\s+/g, ' ').trim();
    blueprintSnippet = plainText.substring(0, 400) + (plainText.length > 400 ? '...' : '');
  }

  return {
    parentName: window.parentFirstName || 'Unknown',
    parentEmail: window.parentEmail || '',
    date: new Date().toLocaleDateString('en-US', {
      year: 'numeric', month: 'long', day: 'numeric'
    }),
    timestamp: new Date().toISOString(),
    messageCount: conversationHistory.filter(t => t.role === 'assistant').length,
    blueprintSnippet: blueprintSnippet,
    fullTranscript: readableTranscript,
    finalMessage: finalMessage,
    // Wrapper-captured values. These are reliable — they came directly from
    // the parent's typed answers, not from the model's memory.
    blueprintTier: window.capturedTier || '',
    patternPicked: window.capturedPattern || '',
    f1Score: (window.capturedFeedback && window.capturedFeedback.F1) || '',
    f2Score: (window.capturedFeedback && window.capturedFeedback.F2) || '',
    f3Score: (window.capturedFeedback && window.capturedFeedback.F3) || '',
    f4Score: (window.capturedFeedback && window.capturedFeedback.F4) || '',
    f5Answer: (window.capturedFeedback && window.capturedFeedback.F5) || '',
    f6Answer: (window.capturedFeedback && window.capturedFeedback.F6) || '',
  };
}

async function sendTranscriptToMake() {
  // Guard against double-sends. Check BOTH the in-memory flag and the
  // localStorage persisted state — if either says we already sent, skip.
  // This is defensive against race conditions where a turn fires twice
  // before the in-memory flag has been set, or where a refresh wiped
  // the in-memory flag but localStorage still says it was sent.
  if (window.transcriptSent) {
    console.log('Transcript already sent (in-memory) — skipping duplicate send.');
    return;
  }
  try {
    const persisted = JSON.parse(localStorage.getItem(SESSION_STORAGE_KEY) || '{}');
    if (persisted.transcriptSent) {
      console.log('Transcript already sent (persisted) — skipping duplicate send.');
      window.transcriptSent = true; // sync the in-memory flag
      return;
    }
  } catch (err) {
    // localStorage read failed — proceed with send. Better one extra email
    // than no email at all if storage is broken.
    console.warn('Could not check persisted transcriptSent flag:', err);
  }

  if (MAKE_WEBHOOK_URL === 'PASTE_YOUR_MAKE_WEBHOOK_URL_HERE') {
    console.error('Make webhook URL not configured. Skipping transcript send.');
    console.error('See chat.js MAKE_WEBHOOK_URL constant for setup instructions.');
    return;
  }

  // Sanity check before firing — defense against blank-email events.
  // A real completed interview produces 20+ assistant turns and has a parent
  // name and email captured. If the payload is missing those, something went
  // wrong (premature sentinel, state loss, refresh corruption) and sending
  // a blank email would corrupt Jay's data. Block the send and log loudly.
  const assistantTurnCount = conversationHistory.filter(t => t.role === 'assistant').length;
  const hasParentName = window.parentFirstName && window.parentFirstName.trim() !== '';
  const MIN_TURNS_FOR_REAL_INTERVIEW = 15; // a real interview has 22+ turns; 15 is a generous floor

  if (!hasParentName) {
    console.error('Webhook fire BLOCKED: parent name is empty. This indicates state loss or premature trigger.');
    console.error('Conversation length:', assistantTurnCount, 'turns. parentFirstName:', window.parentFirstName);
    return;
  }
  if (assistantTurnCount < MIN_TURNS_FOR_REAL_INTERVIEW) {
    console.error('Webhook fire BLOCKED: only', assistantTurnCount, 'assistant turns. Real interviews have 20+. Sentinel may have fired prematurely.');
    return;
  }

  // Set the flag and persist BEFORE the network request, so any concurrent
  // retry that fires while we're awaiting the fetch will see the flag set.
  window.transcriptSent = true;
  saveSession();

  const payload = buildTranscriptPayload();

  try {
    const response = await fetch(MAKE_WEBHOOK_URL, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(payload),
    });

    if (response.ok) {
      console.log('Transcript sent to Make successfully.');
    } else {
      console.error('Make webhook returned non-OK status:', response.status);
      // Re-arm so the parent could potentially trigger a retry by sending another message.
      // (In practice this rarely matters since the interview is over.)
      window.transcriptSent = false;
    }
  } catch (error) {
    console.error('Failed to send transcript to Make:', error);
    window.transcriptSent = false;
  }
}
