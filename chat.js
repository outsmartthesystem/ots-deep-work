function estimateQuestionProgress() {
  // Count assistant messages, subtract 1 for the opening, that's roughly the question we're on.
  // Cap at 22 since that's the max numbered question in v8.
  const assistantTurns = conversationHistory.filter(m => m.role === 'assistant').length;
  const estimate = Math.max(1, assistantTurns - 1);
  return Math.min(estimate, 22);
}

function updateProgress() {
  // Whisper-thin amber bar under the chat header. Driven by the same turn-count
  // estimate the interviewer note uses. Pre-Blueprint it caps at 96% so it never
  // claims "done" early; starts at a visible 4% so the parent sees life from turn one.
  const fill = document.getElementById('progressFill');
  if (!fill) return;
  const pct = window.blueprintDelivered
    ? 100
    : Math.min(96, 4 + Math.round((estimateQuestionProgress() / 22) * 92));
  fill.style.width = pct + '%';
}

function isBlueprintTrigger(userText) {
  const lower = userText.toLowerCase().trim();
  // Explicit triggers are wrap-up signals. Anything that could plausibly
  // appear as a normal answer to a late-interview question is excluded.
  // 'no' was previously here and removed in v12.8 because it's the most
  // common answer to Q22 (the omit-anything check) and was firing false
  // positive Blueprint generation. The model still produces the Blueprint
  // correctly when the parent legitimately wraps up — the prompt has its
  // own wrap-up logic; this function is just for budget-bumping.
  const explicitTriggers = ['wrap it up', 'wrap up', 'wrapup', "i'm done", 'im done', 'ready', 'finalize', 'go ahead'];
  return explicitTriggers.some(t => lower === t || lower === t + '.' || lower.startsWith(t + ' ') || lower.endsWith(' ' + t));
}

function isLateInInterview() {
  // Once we're past roughly Q18 in a 22-question interview, any user message could realistically
  // trigger the Blueprint. We bump the token budget so the Blueprint never gets truncated
  // mid-sentence, regardless of whether the user phrased their final answer as "wrap it up".
  const assistantTurns = conversationHistory.filter(m => m.role === 'assistant').length;
  return assistantTurns >= 18;
}

async function sendMessage() {
  const input = document.getElementById('userInput');
  const sendButton = document.getElementById('sendButton');
  const userText = input.value.trim();
  if (!userText || sendButton.disabled) return;

  addMessage(userText, 'user');
  input.value = '';
  autoResize(input);
  sendButton.disabled = true;

  // Determine token budget AND whether we're about to generate a Blueprint, so
  // the thinking indicator can show a reassuring message instead of silent dots.
  const blueprintIncoming = isBlueprintTrigger(userText);
  const lateInterview = isLateInInterview();
  const showBlueprintLoadingMessage = blueprintIncoming || (lateInterview && userText.length < 30);

  const thinking = addThinking(showBlueprintLoadingMessage);

  // Inject a soft progress note into the user's message so the model stays anchored
  // to the numbered question it should be on. This is invisible to the user — it goes
  // into the conversation history that the API sees.
  let messageForApi = userText;
  if (!window.blueprintDelivered) {
    const currentQ = estimateQuestionProgress();
    const nextQ = Math.min(currentQ + 1, 27);
    messageForApi = `[Internal note for the interviewer: You are roughly on Q${currentQ} of 22. After acknowledging this answer in 1-3 short sentences, move to Q${nextQ}. Do not extend with additional follow-ups. Do not return to earlier topics. Vary your response shape between A (reflect+ask), B (hold+ask), and C (acknowledge+ask) — do not use the same shape twice in a row. AT GENUINE PEAKS ONLY (4-8 times across the full interview), you may use a discovery move (Stop, Stitch, or Marking) with the four-part structure — but only when the parent's answer is a genuine arrival, not when it's merely articulate. Default to the three shapes; reserve discovery moves for moments of real weight. IF a catastrophic disclosure surfaces (death of a child, suicide, abuse, terminal illness, etc.), the Catastrophic Disclosure Protocol overrides all other response shapes — acknowledge with weight, name that it exceeded the question, offer the parent a choice to stay or move on, and wait for their signal. Track engagement signals as you go: first-person ownership, specific memories, real body responses, weight in admissions. These determine the Blueprint tier at the end.]\n\n${userText}`;
  }

  // Capture F1-F6 score answers and Q12 pattern pick at the wrapper level.
  // detectAndUpdateActiveQuestion() ran on the previous assistant message and
  // set window.activeFeedbackQuestion (or activePatternQuestion). This call
  // stores the parent's typed answer into capturedFeedback or capturedPattern.
  captureUserAnswer(userText);

  conversationHistory.push({ role: 'user', content: messageForApi });

  // Token budget. Blueprint generation needs lots of room. Conversational turns are tight.
  let tokenBudget;
  if (window.blueprintDelivered) {
    tokenBudget = 1500; // Feedback Q&A after blueprint
  } else if (blueprintIncoming || lateInterview) {
    tokenBudget = 6000; // Blueprint can be long; give it room
  } else {
    tokenBudget = 1200; // Conversational turn — keep responses tight
  }

  try {
    let data = null;
    let attempts = 0;
    const maxAttempts = 4;

    while (attempts < maxAttempts) {
      attempts++;

      // Compute the current date and substitute it into the system prompt before sending.
      // The system prompt has TWO date placeholder formats — handle both.
      // [Today's date] is used in the Blueprint output template.
      // [TODAY'S DATE] is used in the FEEDBACK FOR JAY block instruction.
      // Both must be replaced or the model either hallucinates a date from training-data
      // priors (producing "January 2025") or copies the bracketed placeholder verbatim
      // into the parent-visible output.
      const today = new Date().toLocaleDateString('en-US', {
        year: 'numeric', month: 'long', day: 'numeric'
      });
      let systemPromptWithSubstitutions = SYSTEM_PROMPT
        .replace(/\[Today's date\]/g, today)
        .replace(/\[TODAY'S DATE\]/g, today);

      // Substitute the parent's first name into the FEEDBACK FOR JAY block. The system prompt
      // has a literal "[PARENT FIRST NAME]" placeholder; without this substitution the model
      // copies the bracketed text verbatim into the final output ("FEEDBACK FOR JAY — [PARENT
      // FIRST NAME] — May 7, 2026") instead of filling in the actual name.
      if (window.parentFirstName) {
        systemPromptWithSubstitutions = systemPromptWithSubstitutions.replace(
          /\[PARENT FIRST NAME\]/g, window.parentFirstName
        );
      }

      // Substitute the wrapper-captured values for feedback scores, pattern pick,
      // and tier. The prompt's FEEDBACK FOR JAY block instructions reference
      // these placeholders. Live substitution at every API call means the model
      // sees current state — early in the interview most placeholders show as
      // "[not yet captured]"; by the FEEDBACK FOR JAY moment, all are populated.
      const captures = buildFeedbackSubstitutions();
      Object.keys(captures).forEach(placeholder => {
        const escaped = placeholder.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
        systemPromptWithSubstitutions = systemPromptWithSubstitutions.replace(
          new RegExp(escaped, 'g'), captures[placeholder]
        );
      });

      const response = await fetch('/api/chat', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          model: 'claude-sonnet-4-6',
          max_tokens: tokenBudget,
          system: systemPromptWithSubstitutions,
          messages: getMessages(),
          useFastModel: window.blueprintDelivered
        })
      });

      data = await response.json();

      if (data.error && data.error.type === 'rate_limit_error') {
        if (attempts < maxAttempts) {
          thinking.remove();
          const waitSeconds = attempts * 20;
          const waitEl = addMessage('One moment — taking a short pause before continuing...', 'thinking');
          await new Promise(resolve => setTimeout(resolve, waitSeconds * 1000));
          waitEl.remove();
          const newThink = addThinking();
          thinking.remove = newThink.remove.bind(newThink);
          continue;
        } else {
          thinking.remove();
          addMessage('We hit a brief pause. Please type your last message again and we will continue right where we left off.', 'agent');
          sendButton.disabled = false;
          return;
        }
      }
      break;
    }

    if (!data || !data.content || !data.content[0]) {
      throw new Error(JSON.stringify(data));
    }

    const rawAssistantMessage = data.content[0].text;
    thinking.remove();

    // Capture the tier from the [BLUEPRINT_TIER:N] sentinel if present, and
    // strip the sentinel from the message before storing or rendering it.
    // This must happen before any other processing so the parent never sees
    // the sentinel and the wrapper has the tier locked in.
    const assistantMessage = captureTierFromAssistantMessage(rawAssistantMessage);

    // Check whether this message signals interview completion. If yes, we'll
    // strip the sentinel from what the parent sees and trigger the webhook
    // to send the full transcript to Make. The completion check has to happen
    // BEFORE we render or store the message, so the displayed and stored
    // versions don't include the sentinel.
    const interviewComplete = isInterviewComplete(assistantMessage);
    const cleanedMessage = interviewComplete ? stripSentinel(assistantMessage) : assistantMessage;

    // Store the clean user text in history (without the internal note) so the displayed
    // history stays clean if anything ever surfaces it. The API has already seen the noted version.
    // We replace the last user message with the clean version after a successful response.
    conversationHistory[conversationHistory.length - 1] = { role: 'user', content: userText };

    // The conversation history stores the FULL message (including the FEEDBACK FOR
    // JAY block on the final turn). The webhook reads from history, so Jay's email
    // gets the complete data. But the parent should not see the FEEDBACK FOR JAY
    // block in their chat — it's internal tooling, not parent-facing content.
    // Store the full cleaned message; render only the display-stripped version.
    conversationHistory.push({ role: 'assistant', content: cleanedMessage });
    const displayMessage = interviewComplete
      ? stripFeedbackBlockForDisplay(cleanedMessage)
      : cleanedMessage;
    renderAssistantMessage(displayMessage);

    // After the assistant message renders, detect whether it's asking F1-F6
    // or Q12 (pattern pick). This sets window.activeFeedbackQuestion or
    // window.activePatternQuestion so the next user message gets captured
    // correctly. Detection runs on the cleaned message text (full version
    // so detection can match phrases that appear in the FEEDBACK FOR JAY block
    // if the model emits an F-question there for some reason).
    detectAndUpdateActiveQuestion(cleanedMessage);

    // Save session state after every successful turn so the parent can recover
    // from a refresh or crash. saveSession() is cheap — milliseconds — so
    // running it on every turn has negligible cost.
    saveSession();
    updateProgress();

    // If the interview is complete, fire the webhook to send the transcript to Make.
    // This happens AFTER the message renders so the parent sees the closing message
    // immediately. The webhook fires asynchronously in the background — the parent
    // doesn't wait for it.
    if (interviewComplete) {
      sendTranscriptToMake();
      // Once the transcript has been sent, the session is complete. We keep
      // the localStorage entry briefly so a refresh in the next minute still
      // shows the Blueprint, but it's marked transcriptSent so the next page
      // load won't offer to resume. clearSession will be called on next load.
      saveSession(); // re-save to capture transcriptSent: true
    }

  } catch (error) {
    thinking.remove();
    addMessage('We hit a brief technical pause. Please type your last message again and we will continue right where we left off.', 'agent');
    console.error('Full error:', error);
  }

  sendButton.disabled = false;
  scrollToBottom();
}

function isBlueprint(text) {
  return text.includes('blueprint-container') || text.includes('blueprint-section-header');
}

// ─────────────────────────────────────────────────────────────────────────────
// SAFE RENDERING
// ─────────────────────────────────────────────────────────────────────────────
// Helpers that safely render text and HTML without using innerHTML on
// untrusted content. User input and model output are both treated as untrusted
// by default. The Blueprint is the only HTML allowed through, and it goes
// through a strict allowlist sanitizer first.

function appendTextWithLineBreaks(parent, text) {
  // Render text into a DOM element, converting \n into <br> elements and
  // recognizing a tiny allowlist of inline tags (<em>, <strong>) that the
  // model and our own opening string use for emphasis. Everything else —
  // any other tags, attributes, scripts — gets escaped as text. This
  // preserves the italics in the recognition arc without using innerHTML
  // on untrusted content.
  //
  // The implementation: use a regex to match the allowed tags and route
  // matched content through createElement, while non-matched content goes
  // through createTextNode. Either way, no markup ever reaches innerHTML.

  const lines = text.split('\n');
  lines.forEach((line, lineIndex) => {
    if (lineIndex > 0) {
      parent.appendChild(document.createElement('br'));
    }

    // Match <em>...</em> or <strong>...</strong> spans. Anything else is text.
    const segmentRegex = /<(em|strong)>([\s\S]*?)<\/\1>/gi;
    let lastIndex = 0;
    let match;

    while ((match = segmentRegex.exec(line)) !== null) {
      // Append any text before this match as a plain text node.
      if (match.index > lastIndex) {
        parent.appendChild(document.createTextNode(line.substring(lastIndex, match.index)));
      }
      // Create the inline element via createElement and put the inner content
      // in as a text node — never innerHTML, so nested markup is impossible.
      const tag = match[1].toLowerCase();
      const innerText = match[2];
      const el = document.createElement(tag);
      el.textContent = innerText;
      parent.appendChild(el);
      lastIndex = segmentRegex.lastIndex;
    }

    // Append any trailing text after the last match.
    if (lastIndex < line.length) {
      parent.appendChild(document.createTextNode(line.substring(lastIndex)));
    }
  });
}

function sanitizeBlueprintHTML(html) {
  // Strict allowlist sanitizer for Blueprint output. The Blueprint uses a
  // known set of tags and attributes — anything outside that set is removed.
  // This protects against the model being coaxed into emitting unexpected
  // markup, and against any future Blueprint template changes that might
  // introduce risk.

  const allowedTags = new Set([
    'div', 'p', 'h1', 'h2', 'h3', 'h4',
    'em', 'strong', 'b', 'i', 'br', 'hr',
    'ul', 'ol', 'li', 'blockquote', 'span',
    'button', 'a' // button is for the PDF download; a is for booking link.
  ]);
  const allowedAttributes = new Set(['class', 'style', 'href', 'target', 'rel', 'data-action']);
  const allowedStyleProperties = new Set([
    'color', 'background-color', 'font-style', 'font-weight',
    'text-align', 'margin', 'padding', 'opacity'
  ]);

  // Parse the HTML in a document fragment so we can walk and filter it.
  const template = document.createElement('template');
  template.innerHTML = html;

  function clean(node) {
    // Walk children in reverse so removals don't shift indexes.
    const children = Array.from(node.children);
    children.forEach(child => {
      const tag = child.tagName.toLowerCase();

      if (!allowedTags.has(tag)) {
        // Replace disallowed tag with its text content. This neutralizes
        // <script>, <iframe>, <img onerror=...>, etc., while preserving
        // any text the model wrote inside them.
        const text = document.createTextNode(child.textContent);
        node.replaceChild(text, child);
        return;
      }

      // Filter attributes to the allowlist.
      const attrs = Array.from(child.attributes);
      attrs.forEach(attr => {
        if (!allowedAttributes.has(attr.name.toLowerCase())) {
          child.removeAttribute(attr.name);
        }
      });

      // For style attribute, filter individual properties to the allowlist.
      if (child.hasAttribute('style')) {
        const style = child.getAttribute('style');
        const safeStyles = style.split(';')
          .map(s => s.trim())
          .filter(s => {
            const [prop] = s.split(':').map(x => x.trim().toLowerCase());
            return prop && allowedStyleProperties.has(prop);
          })
          .join('; ');
        if (safeStyles) {
          child.setAttribute('style', safeStyles);
        } else {
          child.removeAttribute('style');
        }
      }

      // For href attribute, only allow http://, https://, mailto: schemes.
      // javascript: URLs would re-open the XSS surface — strip them.
      if (child.hasAttribute('href')) {
        const href = child.getAttribute('href').trim().toLowerCase();
        const safe = href.startsWith('http://') || href.startsWith('https://') ||
                     href.startsWith('mailto:') || href.startsWith('#') || href === '';
        if (!safe) {
          child.removeAttribute('href');
        }
      }

      // Recurse into the child.
      clean(child);
    });
  }

  clean(template.content);
  return template.innerHTML;
}

function renderAssistantMessage(text) {
  const messages = document.getElementById('messages');

  if (isBlueprint(text)) {
    const startIdx = text.indexOf('<div class="blueprint-container">');
    if (startIdx > 0) {
      // The preamble before the Blueprint is plain text from the model.
      // Render it safely with textContent + <br> elements.
      const preamble = document.createElement('div');
      preamble.className = 'message agent';
      preamble.style.cssText = 'animation: fadeUp 0.4s ease forwards; opacity: 0;';
      appendTextWithLineBreaks(preamble, text.substring(0, startIdx));
      messages.appendChild(preamble);
    }

    // The Blueprint itself is HTML by design. Sanitize it through the
    // allowlist before inserting, so any unexpected tags are stripped.
    const blueprintDiv = document.createElement('div');
    blueprintDiv.style.cssText = 'animation: fadeUp 0.5s ease forwards; opacity: 0;';
    const html = startIdx !== -1 ? text.substring(startIdx) : text;
    const sanitizedHTML = sanitizeBlueprintHTML(html);
    blueprintDiv.innerHTML = sanitizedHTML;
    messages.appendChild(blueprintDiv);

    // The sanitizer strips the onclick="" attribute (correctly — that prevents
    // arbitrary script execution from model output). Re-attach the download
    // handler programmatically here. The .download-btn class was preserved.
    const downloadButtons = blueprintDiv.querySelectorAll('.download-btn');
    downloadButtons.forEach(btn => {
      btn.addEventListener('click', downloadBlueprint);
    });

    window.blueprintHTML = sanitizedHTML;
    window.blueprintDelivered = true;

    // Extract plain text for transcript saving via textContent (never innerText
    // since innerText can trigger reflow and is browser-inconsistent).
    const tempDiv = document.createElement('div');
    tempDiv.innerHTML = sanitizedHTML;
    const blueprintText = tempDiv.textContent || '';
    saveTranscript(blueprintText);

  } else {
    // Regular assistant messages are plain text. Render with textContent
    // and proper <br> elements — never innerHTML.
    const div = document.createElement('div');
    div.className = 'message agent';
    div.style.cssText = 'animation: fadeUp 0.4s ease forwards; opacity: 0;';
    appendTextWithLineBreaks(div, text);
    messages.appendChild(div);
  }

  scrollToBottom();
}

function addMessage(text, type) {
  // Renders a user message OR a system status message. Both go through
  // safe text rendering — the user's typed content can never reach the
  // DOM as markup, regardless of what they typed.
  const messages = document.getElementById('messages');
  const div = document.createElement('div');
  div.className = 'message ' + type;
  div.style.cssText = 'animation: fadeUp 0.4s ease forwards; opacity: 0;';
  appendTextWithLineBreaks(div, text);
  messages.appendChild(div);
  scrollToBottom();
  return div;
}

function addThinking(blueprintMode = false) {
  const messages = document.getElementById('messages');
  const div = document.createElement('div');
  div.className = 'message thinking';
  if (blueprintMode) {
    // Blueprint generation takes 30-60 seconds. A blank thinking-dots indicator leaves
    // the parent wondering if the system broke. Replace it with a reassuring message
    // that matches the conversational frame held throughout the interview.
    div.innerHTML = 'Working on your Blueprint now. This will take about a minute. <span class="thinking-dots"></span>';
    div.style.fontStyle = 'italic';
    div.style.opacity = '0.85';
  } else {
    div.innerHTML = '<span class="thinking-dots"></span>';
  }
  messages.appendChild(div);
  scrollToBottom();
  return div;
}

function scrollToBottom() {
  const area = document.getElementById('messagesArea');
  area.scrollTop = area.scrollHeight;
}

function scrollToTop() {
  const area = document.getElementById('messagesArea');
  area.scrollTop = 0;
}

function autoResize(el) {
  el.style.height = 'auto';
  el.style.height = Math.min(el.scrollHeight, 140) + 'px';
}

async function saveTranscript(blueprintText) {
  try {
    await fetch('/api/save-transcript', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        parentName: window.parentName || 'Unknown',
        parentEmail: window.parentEmail || '',
        messages: conversationHistory,
        blueprintText: blueprintText,
        timestamp: new Date().toISOString()
      })
    });
    console.log('Transcript saved');
  } catch (err) {
    console.error('Failed to save transcript:', err);
  }
}

async function downloadBlueprint() {
  if (!window.blueprintHTML) { alert('Blueprint not yet generated.'); return; }
  const btn = document.querySelector('.download-btn');
  if (btn) { btn.textContent = 'Preparing PDF...'; btn.disabled = true; }

  const pdfDiv = document.getElementById('pdf-blueprint');
  const nameEl = document.querySelector('.blueprint-name');
  const parentName = nameEl ? nameEl.textContent : 'Family';

  pdfDiv.innerHTML = `
    <div class="pdf-header">
      <p class="pdf-eyebrow">Outsmart the System &bull; Family Money Story Interview</p>
      <p class="pdf-title">Family Money Story Blueprint</p>
      <p class="pdf-date">Prepared for ${parentName}</p>
    </div>
    ${convertBlueprintToPDF(window.blueprintHTML)}
    <div class="pdf-footer">Outsmart the System &mdash; outsmartthesystem.org</div>
  `;
  pdfDiv.style.display = 'block';

  const opt = {
    margin: [12, 12, 12, 12],
    filename: 'OTS-Family-Money-Story-Blueprint.pdf',
    image: { type: 'jpeg', quality: 0.98 },
    html2canvas: { scale: 2, useCORS: true, backgroundColor: '#ffffff' },
    jsPDF: { unit: 'mm', format: 'a4', orientation: 'portrait' }
  };

  try { await html2pdf().set(opt).from(pdfDiv).save(); } catch(e) { console.error('PDF error:', e); }
  pdfDiv.style.display = 'none';
  if (btn) { btn.textContent = 'Download Your Blueprint as PDF'; btn.disabled = false; }
}

function convertBlueprintToPDF(html) {
  return html
    .replace(/class="blueprint-container"/g, 'style="font-family: Georgia, serif; padding: 0;"')
    .replace(/class="blueprint-title"/g, 'class="pdf-eyebrow"')
    .replace(/class="blueprint-name"/g, 'class="pdf-title"')
    .replace(/class="blueprint-date"/g, 'class="pdf-date"')
    .replace(/class="blueprint-rule"/g, 'class="pdf-rule"')
    .replace(/class="blueprint-section-header"/g, 'class="pdf-section-label"')
    .replace(/class="blueprint-body"/g, 'class="pdf-body"')
    .replace(/class="blueprint-quote"/g, 'class="pdf-quote"')
    .replace(/class="blueprint-cry-quote"/g, 'class="pdf-cry-quote"')
    .replace(/class="blueprint-signature"/g, 'class="pdf-signature"')
    .replace(/class="blueprint-cta"/g, 'class="pdf-cta"')
    .replace(/class="blueprint-cta-heading"/g, 'class="pdf-section-label"')
    .replace(/<button class="download-btn"[^>]*>.*?<\/button>/gs, '');
}

function startSession() {
  // Guard against double-starts (double Enter press or double-click on Begin).
  if (window.sessionStarted) return;

  const nameInput = document.getElementById('inputName');
  const emailInput = document.getElementById('inputEmail');
  const nameError = document.getElementById('nameError');
  const emailError = document.getElementById('emailError');
  const name = nameInput.value.trim();
  const email = emailInput.value.trim();

  // Reset any prior validation state before re-checking.
  if (nameError) nameError.classList.remove('visible');
  if (emailError) emailError.classList.remove('visible');
  nameInput.style.borderBottomColor = '';
  emailInput.style.borderBottomColor = '';

  if (!name) {
    nameInput.focus();
    nameInput.style.borderBottomColor = '#d4a574';
    if (nameError) nameError.classList.add('visible');
    return;
  }

  // The Blueprint is delivered by email — a missing or malformed address means
  // 35-45 minutes of work with no deliverable, so require a plausible email.
  if (!email || !/^[^\s@]+@[^\s@]+\.[^\s@]{2,}$/.test(email)) {
    emailInput.focus();
    emailInput.style.borderBottomColor = '#d4a574';
    if (emailError) emailError.classList.add('visible');
    return;
  }

  window.sessionStarted = true;
  const beginBtn = document.getElementById('beginButton');
  if (beginBtn) { beginBtn.disabled = true; beginBtn.textContent = 'Beginning…'; }

  window.parentName = name;
  window.parentEmail = email;

  document.getElementById('chat-heading').textContent = name + "'s Interview";
  document.getElementById('intake-screen').style.display = 'none';
  const chatScreen = document.getElementById('chat-screen');
  chatScreen.style.display = 'flex';

  const opening = "You want your kids ready for real life, not just good at school. School can teach academics. It does not reliably teach money, responsibility, or judgment under pressure.\n\nIf you've ever thought <em>\"my kids are smart, but I'm not sure they're ready for real life\"</em> \u2014 this interview is for you. Real change in a family doesn't start with the kid. It starts with the money story the parent is living.\n\nThis is the Family Money Story Interview, the front door to Outsmart the System, run by Jay Bhakta. In the next 35 to 45 minutes, you'll see the money pattern you inherited, what your kids are absorbing, and where the work begins. By the end, you'll have a custom Blueprint and a clear next step.\n\nThis is deep work, not budgeting tips. There are no right answers. You can skip any question. You can pause anytime.\n\nFind a quiet room. Phone away. Do this alone \u2014 you can talk to your spouse after.\n\nLet's start simple. Who is under your roof right now? First names, ages, and one sentence about each child that only a parent would know.";

  conversationHistory.push({ role: 'user', content: 'My name is ' + name + '.' });
  conversationHistory.push({ role: 'assistant', content: opening });

  // Store the parent's name globally so the wrapper can substitute it into the system
  // prompt at API call time. The system prompt has a literal "[PARENT FIRST NAME]"
  // placeholder in the FEEDBACK FOR JAY block; without this substitution the model
  // copies the bracketed text verbatim instead of filling in the actual name.
  window.parentFirstName = name;

  // Generate a fresh session ID and save initial state so the parent can recover
  // if they refresh, lose connection, or accidentally close the tab.
  window.sessionId = generateSessionId();
  saveSession();

  renderAssistantMessage(opening);
  // Override the scrollToBottom that just fired in renderAssistantMessage().
  // On the very first render, we want the parent to see the TOP of the opening
  // message — not the bottom of it. Subsequent messages keep using scrollToBottom.
  setTimeout(scrollToTop, 50);
  updateProgress();
  document.getElementById('userInput').focus();
}

